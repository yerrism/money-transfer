CREATE SCHEMA IF NOT EXISTS MT;
CREATE TABLE IF NOT EXISTS MT.ACCOUNT( "ACC_OWNER" VARCHAR(100),
			"ACC_NUMBER" VARCHAR(50) NOT NULL,
			"BALANCE" DECIMAL(100,2),
			"CURRENCY" VARCHAR(3)
);

CREATE TABLE IF NOT EXISTS MT.TRANSACTION(
			"ID" IDENTITY,
			"ACC_NUMBER" VARCHAR(50) NOT NULL,
			"TXN_TIMESTAMP" TIMESTAMP,
			"DESCRIPTION" VARCHAR(100),
			"BALANCE" DECIMAL(100,2),
			"PAYMENT_IN" DECIMAL(100,2),
			"PAYMENT_OUT" DECIMAL(100,2)
);

ALTER TABLE MT.ACCOUNT ADD PRIMARY KEY (ACC_NUMBER);
ALTER TABLE MT.TRANSACTION ADD FOREIGN KEY (ACC_NUMBER) REFERENCES MT.ACCOUNT(ACC_NUMBER);

